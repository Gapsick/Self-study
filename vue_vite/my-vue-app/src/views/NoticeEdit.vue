<template>
    <div>
      <h2>공지사항 수정</h2>
      <form @submit.prevent="updateNoticeData">
        <label>제목</label>
        <input type="text" v-model="notice.title" required />
  
        <label>내용</label>
        <textarea v-model="notice.content" required></textarea>
  
        <label>학년</label>
        <select v-model="notice.academic_year">
          <option value="">전체</option>
          <option value="1">1학년</option>
          <option value="2">2학년</option>
          <option value="3">3학년</option>
        </select>
  
        <label>공지 고정</label>
        <input type="checkbox" v-model="notice.is_pinned" />
  
        <!-- 🔹 기존 파일 미리보기 및 삭제 옵션 -->
        <div v-if="notice.file_path">
          <p>기존 파일: <a :href="`http://localhost:5000/${notice.file_path}`" target="_blank">{{ getFileName(notice.file_path) }}</a></p>
          <button type="button" @click="removeExistingFile">파일 삭제</button>
        </div>
  
        <!-- 🔹 새 파일 업로드 -->
        <label>파일 업로드</label>
        <input type="file" @change="handleFileUpload" />
  
        <button type="submit">저장</button>
        <button type="button" @click="cancelEdit">취소</button>
      </form>
    </div>
  </template>
  
  <script>
  import { ref, onMounted } from "vue";
  import { useRoute, useRouter } from "vue-router";
  import { fetchNoticeDetail, updateNotice } from "@/api/noticeApi";
  
  export default {
    setup() {
      const route = useRoute();
      const router = useRouter();
      const notice = ref({});
      const newFile = ref(null);
      const removeFile = ref(false); // 파일 삭제 여부
  
      // 🔹 공지사항 상세 정보 가져오기
      onMounted(async () => {
        const data = await fetchNoticeDetail(route.params.id);
        if (data) {
          notice.value = data;
        }
      });
  
      // 🔹 파일 업로드 핸들러
      const handleFileUpload = (event) => {
        newFile.value = event.target.files[0];
        removeFile.value = false; // 새 파일 선택 시 기존 파일 삭제 안 함
      };
  
      // 🔹 기존 파일 삭제 요청
      const removeExistingFile = () => {
        removeFile.value = true;
        notice.value.file_path = null; // UI에서 파일 표시 제거
      };
  
      // 🔹 공지사항 업데이트 요청
      const updateNoticeData = async () => {
        const formData = new FormData();
        formData.append("title", notice.value.title);
        formData.append("content", notice.value.content);
        formData.append("academic_year", notice.value.academic_year || "");
        formData.append("is_pinned", notice.value.is_pinned ? "1" : "0");
  
        if (newFile.value) {
          formData.append("file", newFile.value);
        }
  
        if (removeFile.value) {
          formData.append("removeFile", "true"); // ✅ 삭제 요청 추가
        }
  
        console.log("📢 (NoticeEdit.vue) 전송할 FormData:", [...formData.entries()]);
  
        const response = await updateNotice(route.params.id, formData);
        if (!response.error) {
          alert("공지사항이 수정되었습니다.");
          router.push(`/notices/${route.params.id}`);
        } else {
          alert("수정 실패: " + response.error);
        }
      };
  
      // 🔹 파일명 추출
      const getFileName = (filePath) => {
        return filePath ? filePath.split("/").pop() : "";
      };
  
      // 🔹 수정 취소
      const cancelEdit = () => {
        router.push(`/notices/${route.params.id}`);
      };
  
      return {
        notice,
        newFile,
        removeFile,
        handleFileUpload,
        removeExistingFile,
        updateNoticeData,
        getFileName,
        cancelEdit,
      };
    },
  };
  </script>
  